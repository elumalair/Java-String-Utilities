public String convertToUpperCase( final String name) {
    return name.toUpperCase();
}
 
public String convertToLowerCase( final String name) {
    return name.toLowerCase();
}
 
public String trimSpaces( final String name) {
    return name.trim();
}
 
public int calculateLength( final String name) {
    return name.length();
}
 
public boolean isEmpty( final String name) {
    return name.isEmpty();
}
